package io.github.yokigroup.view.render;

/**
 * Enum representing the various states the program can be in. This state influences what is displayed on screen.
 */
public enum RenderState {
    /**
     * World render state.
     */
    WORLD,
    /**
     * Fight render state.
     */
    FIGHT,
    /**
     * Death render state.
     */
    DEATH,
    /**
     * Victory render state.
     */
    VICTORY
}
